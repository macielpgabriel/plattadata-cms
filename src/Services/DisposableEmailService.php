    private static ?array $domains = null;

    public function isDisposable(string $email): bool
    {
        $domain = $this->extractDomain($email);
        if ($domain === '') {
            return false;
        }

        if (self::$domains === null) {
            self::$domains = array_flip(self::DISPOSABLE_DOMAINS);
        }

        return isset(self::$domains[$domain]);
    }

    public function getDisposableDomains(): array
    {
        return self::DISPOSABLE_DOMAINS;
    }

    private function extractDomain(string $email): string
    {
        $email = strtolower(trim($email));
        $parts = explode('@', $email);
        
        if (count($parts) !== 2) {
            return '';
        }

        $domain = $parts[1];
        
        if (str_starts_with($domain, 'www.')) {
            $domain = substr($domain, 4);
        }

        return $domain;
    }
